# ── Цвета ──────────────────────────────────────────────────────────────────
C_TEAL_DARK   = "0D9488"
C_TEAL_MID    = "14B8A6"
C_TEAL_LIGHT  = "CCFBF1"
C_BLUE_DARK   = "0EA5E9"
C_BLUE_LIGHT  = "E0F2FE"
C_AMBER       = "F59E0B"
C_AMBER_LIGHT = "FEF3C7"
C_RED_LIGHT   = "FEE2E2"
C_GREEN_LIGHT = "DCFCE7"
C_GRAY_HEADER = "1E293B"
C_GRAY_ROW    = "F8FAFC"
C_WHITE       = "FFFFFF"

def fill(hex_color):
    return PatternFill("solid", fgColor=hex_color)

def bold(size=11, color="000000", italic=False):
    return Font(name="Calibri", bold=True, size=size, color=color, italic=italic)

def normal(size=10, color="000000", italic=False):
    return Font(name="Calibri", size=size, color=color, italic=italic)

def border():
    s = Side(style="thin", color="CBD5E1")
    return Border(left=s, right=s, top=s, bottom=s)

def wrap_center():
    return Alignment(wrap_text=True, vertical="top", horizontal="center")

def wrap_left():
    return Alignment(wrap_text=True, vertical="top", horizontal="left")

def set_header_row(ws, headers, row=1, col_start=1,
                   bg=C_GRAY_HEADER, fg=C_WHITE, size=11):
    for i, h in enumerate(headers):
        c = ws.cell(row=row, column=col_start + i, value=h)
        c.fill   = fill(bg)
        c.font   = bold(size, fg)
        c.alignment = wrap_center()
        c.border = border()

def style_row(ws, row_num, n_cols, bg=C_WHITE, font_size=10):
    for col in range(1, n_cols + 1):
        c = ws.cell(row=row_num, column=col)
        c.fill      = fill(bg)
        c.font      = normal(font_size)
        c.alignment = wrap_left()
        c.border    = border()

def write_row(ws, row_num, values, bg=C_WHITE, font_size=10,
              bold_cols=None, accent_col=None, accent_bg=None):
    bold_cols = bold_cols or []
    for i, v in enumerate(values):
        col = i + 1
        c = ws.cell(row=row_num, column=col, value=v)
        bg_use = accent_bg if (accent_col and col == accent_col) else bg
        c.fill      = fill(bg_use or bg)
        c.font      = bold(font_size) if col in bold_cols else normal(font_size)
        c.alignment = wrap_left()
        c.border    = border()

def add_section_header(ws, row_num, n_cols, title, bg=C_TEAL_MID):
    ws.merge_cells(
        start_row=row_num, start_column=1,
        end_row=row_num,   end_column=n_cols
    )
    c = ws.cell(row=row_num, column=1, value=f"  {title}")
    c.fill      = fill(bg)
    c.font      = bold(11, C_WHITE)
    c.alignment = Alignment(wrap_text=True, vertical="center", horizontal="left")
    c.border    = border()


