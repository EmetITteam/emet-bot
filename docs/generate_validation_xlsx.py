HEADERS = [
    ("№", 5),
    ("Тип запиту", 22),
    ("Приклад запиту від менеджера", 40),
    ("Що робив бот раніше (помилка)", 40),
    ("Наша пропозиція структури відповіді", 60),
    ("Хто перевіряє", 15),
    ("Статус (+/-)", 12),
    ("Ваші правки до структури", 50),
    ("Що не можна писати (заборони)", 40),
]

def create_intents_sheet(wb, title, fill, intents):
    ws = wb.create_sheet(title)
    ws.sheet_view.showGridLines = False

    # Заголовки
    for col_idx, (name, width) in enumerate(HEADERS, 1):
        cell = ws.cell(row=1, column=col_idx, value=name)
        cell.fill = HEADER_FILL
        cell.font = HEADER_FONT
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        cell.border = BORDER
        ws.column_dimensions[get_column_letter(col_idx)].width = width

    ws.row_dimensions[1].height = 40

    # Заморозити перший рядок
    ws.freeze_panes = "A2"

    # Дані
    for idx, intent in enumerate(intents, 1):
        row = idx + 1
        values = [
            idx,
            intent["name"],
            intent["example"],
            intent["bot_was_wrong"],
            intent["proposed_structure"],
            intent["validator"],
            "",  # status
            "",  # edits
            "",  # forbidden
        ]
        for col_idx, val in enumerate(values, 1):
            cell = ws.cell(row=row, column=col_idx, value=val)
            cell.alignment = WRAP
            cell.border = BORDER
            cell.fill = fill
            if col_idx == 1:
                cell.alignment = CENTER
                cell.font = BOLD_FONT
            else:
                cell.font = NORMAL_FONT

        # Висота рядка залежно від розміру контенту
        max_lines = max(
            intent["proposed_structure"].count("\n"),
            intent["bot_was_wrong"].count("\n"),
            intent["example"].count("\n"),
        )
        ws.row_dimensions[row].height = max(60, (max_lines + 1) * 15)

    return ws


